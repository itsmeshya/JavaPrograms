package Intro.java.magnus;

public class WhileLoop {

	public static void main(String[] args) {
		// While Loop-if we don't know the how many iteration going to be there, then while is best
		int i=1;
		while(i<=10){
			System.out.println(i);
			i++;
		}
		// do while
		int j=5;
		do {
			System.out.println(j);
			j++;
		}
		while(j<=5);
	
	}

}
